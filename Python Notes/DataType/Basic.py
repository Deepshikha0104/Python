a = 10
print(a, type(a)) #10 <class 'int'>

b = 12.33
print(b, type(b)) #12.33 <class 'float'>

c = 'K'
print(c, type(c)) #K <class 'str'>

d = 'Code'
print(d, type(d)) #Code <class 'str'>

e = True
print(e, type(e)) #True <class 'bool'>

h = 3+5j
print(h,type(h)) #(3+5j) <class 'complex'>